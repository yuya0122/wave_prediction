CREATE TABLE PUBLIC.WEATHER_REPORT(
POINT_ID int NOT NULL
,DATE date NOT NULL
,TIME time NOT NULL
,WEATHER varchar
,TEMPERATURE float NOT NULL
,PRECIPITATION float NOT NULL
,WIND_DIRECTION varchar NOT NULL
,WIND_SPEED float NOT NULL
,WAVE_HIGHT float NOT NULL
,WAVE_DIRECTION varchar NOT NULL
,WAVE_PERIOD float NOT NULL
,PRIMARY KEY(POINT_ID,DATE,TIME)
);