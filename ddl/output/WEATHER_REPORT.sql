CREATE TABLE PUBLIC.WEATHER_REPORT(
POINT_ID int NOT NULL
,DATE date NOT NULL
,TIME time NOT NULL
,WEATHER varchar NOT NULL
,TEMPERATURE int NOT NULL
,PRECIPITATION int NOT NULL
,WIND_DIRECTION varchar NOT NULL
,WIND_SPEED int NOT NULL
,WAVE_HIGHT int NOT NULL
,WAVE_DIRECTION varchar NOT NULL
,WAVE_PERIOD int NOT NULL
,PRIMARY KEY(POINT_ID,DATE,TIME)
);